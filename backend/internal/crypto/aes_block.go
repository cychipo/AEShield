var aesRcon = [15]byte{0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36, 0x6C, 0xD8, 0xAB, 0x4D}

type aesBlock struct {
	roundKeys []byte
	rounds    int
}

func newAESBlock(key []byte) (*aesBlock, error) {
	switch len(key) {
	case 16, 24, 32:
	default:
		return nil, ErrInvalidKeyBits
	}

	roundKeys, rounds, err := expandAESKey(key)
	if err != nil {
		return nil, err
	}

	return &aesBlock{roundKeys: roundKeys, rounds: rounds}, nil
}

func (b *aesBlock) BlockSize() int {
	return aesBlockSize
}

func (b *aesBlock) Encrypt(dst, src []byte) {
	if len(src) < aesBlockSize || len(dst) < aesBlockSize {
		panic("crypto: AES block encrypt requires 16-byte src and dst")
	}

	var state [aesBlockSize]byte
	copy(state[:], src[:aesBlockSize])

	addRoundKey(state[:], b.roundKeys[:aesBlockSize])
	for round := 1; round < b.rounds; round++ {
		subBytes(state[:])
		shiftRows(state[:])
		mixColumns(state[:])
		addRoundKey(state[:], b.roundKeys[round*aesBlockSize:(round+1)*aesBlockSize])
	}
	subBytes(state[:])
	shiftRows(state[:])
	addRoundKey(state[:], b.roundKeys[b.rounds*aesBlockSize:(b.rounds+1)*aesBlockSize])

	copy(dst[:aesBlockSize], state[:])
}

func expandAESKey(key []byte) ([]byte, int, error) {
	nk := len(key) / 4
	var rounds int
	switch nk {
	case 4:
		rounds = 10
	case 6:
		rounds = 12
	case 8:
		rounds = 14
	default:
		return nil, 0, fmt.Errorf("crypto: invalid AES key length: %d", len(key))
	}

	totalWords := 4 * (rounds + 1)
	words := make([]byte, totalWords*4)
	copy(words, key)

	for i := nk; i < totalWords; i++ {
		temp := []byte{
			words[(i-1)*4],
			words[(i-1)*4+1],
			words[(i-1)*4+2],
			words[(i-1)*4+3],
		}

		if i%nk == 0 {
			temp = subWord(rotWord(temp))
			temp[0] ^= aesRcon[i/nk]
		} else if nk > 6 && i%nk == 4 {
			temp = subWord(temp)
		}

		for j := 0; j < 4; j++ {
			words[i*4+j] = words[(i-nk)*4+j] ^ temp[j]
		}
	}

	return words, rounds, nil
}

func rotWord(word []byte) []byte {
	return []byte{word[1], word[2], word[3], word[0]}
}

func subWord(word []byte) []byte {
	return []byte{aesSBox[word[0]], aesSBox[word[1]], aesSBox[word[2]], aesSBox[word[3]]}
}

func subBytes(state []byte) {
	for i := range state {
		state[i] = aesSBox[state[i]]
	}
}

func shiftRows(state []byte) {
	state[1], state[5], state[9], state[13] = state[5], state[9], state[13], state[1]
	state[2], state[6], state[10], state[14] = state[10], state[14], state[2], state[6]
	state[3], state[7], state[11], state[15] = state[15], state[3], state[7], state[11]
}

func mixColumns(state []byte) {
	for c := 0; c < 4; c++ {
		off := c * 4
		s0, s1, s2, s3 := state[off], state[off+1], state[off+2], state[off+3]
		state[off] = gmul2(s0) ^ gmul3(s1) ^ s2 ^ s3
		state[off+1] = s0 ^ gmul2(s1) ^ gmul3(s2) ^ s3
		state[off+2] = s0 ^ s1 ^ gmul2(s2) ^ gmul3(s3)
		state[off+3] = gmul3(s0) ^ s1 ^ s2 ^ gmul2(s3)
	}
}

func addRoundKey(state, roundKey []byte) {
	for i := 0; i < aesBlockSize; i++ {
		state[i] ^= roundKey[i]
	}
}

func gmul2(x byte) byte {
	if x&0x80 != 0 {
		return (x << 1) ^ 0x1b
	}
	return x << 1
}

func gmul3(x byte) byte {
	return gmul2(x) ^ x
}
